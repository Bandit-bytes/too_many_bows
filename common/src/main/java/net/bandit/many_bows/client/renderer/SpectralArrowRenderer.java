package net.bandit.many_bows.client.renderer;


import net.bandit.many_bows.entity.SpectralArrow;
import net.minecraft.client.renderer.entity.ArrowRenderer;
import net.minecraft.client.renderer.entity.EntityRendererProvider;
import net.minecraft.resources.ResourceLocation;


public class SpectralArrowRenderer extends ArrowRenderer<SpectralArrow> {
    private static final ResourceLocation TEXTURE = ResourceLocation.fromNamespaceAndPath("too_many_bows", "textures/entity/spectral_arrow.png");


    public SpectralArrowRenderer(EntityRendererProvider.Context context) {
        super(context);
    }

    @Override
    public ResourceLocation getTextureLocation(SpectralArrow entity) {
        return TEXTURE;
    }
}
