package net.bandit.many_bows.client.renderer;


import net.bandit.many_bows.entity.RadiantArrow;
import net.minecraft.client.renderer.entity.ArrowRenderer;
import net.minecraft.client.renderer.entity.EntityRendererProvider;
import net.minecraft.resources.ResourceLocation;
import org.jetbrains.annotations.NotNull;


public class RadianceArrowRenderer extends ArrowRenderer<RadiantArrow> {
    private static final ResourceLocation TEXTURE = new ResourceLocation("too_many_bows", "textures/entity/ancient_sage_arrow.png");

    public RadianceArrowRenderer(EntityRendererProvider.Context context) {
        super(context);
    }

    @Override
    public @NotNull ResourceLocation getTextureLocation(RadiantArrow entity) {
        return TEXTURE;
    }
}
