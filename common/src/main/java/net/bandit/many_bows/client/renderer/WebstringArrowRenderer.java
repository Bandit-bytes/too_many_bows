package net.bandit.many_bows.client.renderer;

import net.bandit.many_bows.entity.WebstringArrow;
import net.minecraft.client.renderer.entity.ArrowRenderer;
import net.minecraft.client.renderer.entity.EntityRendererProvider;
import net.minecraft.resources.ResourceLocation;
import org.jetbrains.annotations.NotNull;


public class WebstringArrowRenderer extends ArrowRenderer<WebstringArrow> {
    private static final ResourceLocation TEXTURE = new ResourceLocation("too_many_bows", "textures/entity/webstring_arrow.png");

    public WebstringArrowRenderer(EntityRendererProvider.Context context) {
        super(context);
    }

    @Override
    public @NotNull ResourceLocation getTextureLocation(WebstringArrow entity) {
        return TEXTURE;
    }
}
