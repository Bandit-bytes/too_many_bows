package net.bandit.many_bows.client.renderer;


import net.bandit.many_bows.entity.VitalityArrow;
import net.minecraft.client.renderer.entity.ArrowRenderer;
import net.minecraft.client.renderer.entity.EntityRendererProvider;
import net.minecraft.resources.ResourceLocation;


public class VitalityArrowRenderer extends ArrowRenderer<VitalityArrow> {
    private static final ResourceLocation TEXTURE = new ResourceLocation("too_many_bows","textures/entity/vitality_arrow.png");

    public VitalityArrowRenderer(EntityRendererProvider.Context context) {
        super(context);
    }

    @Override
    public ResourceLocation getTextureLocation(VitalityArrow entity) {
        return TEXTURE;
    }
}
