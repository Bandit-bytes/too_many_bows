package net.bandit.many_bows.client.renderer;

import net.bandit.many_bows.entity.HunterXPArrow;
import net.minecraft.client.renderer.entity.ArrowRenderer;
import net.minecraft.client.renderer.entity.EntityRendererProvider;
import net.minecraft.resources.ResourceLocation;


public class HunterXPArrowRenderer extends ArrowRenderer<HunterXPArrow> {
    private static final ResourceLocation TEXTURE = ResourceLocation.fromNamespaceAndPath("too_many_bows", "textures/entity/arrow.png");

    public HunterXPArrowRenderer(EntityRendererProvider.Context context) {
        super(context);
    }

    @Override
    public ResourceLocation getTextureLocation(HunterXPArrow entity) {
        return TEXTURE;
    }
}
